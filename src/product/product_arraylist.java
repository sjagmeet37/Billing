package product;


public class product_arraylist {
    
	
int product_id;
String product_name;
String type;
String  manufacturer;
String packing;
String description;

    public product_arraylist(int product_id, String product_name, String type, String manufacturer, String packing, String description) {
        this.product_id = product_id;
        this.product_name = product_name;
        this.type = type;
        this.manufacturer = manufacturer;
        this.packing = packing;
        this.description = description;
    }


}
