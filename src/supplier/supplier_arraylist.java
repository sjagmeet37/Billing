
package supplier;


public class supplier_arraylist {
    
	
int supplier_id;
String supplier_name;
String address;
String city;
String state;
String mobile_no;
String email;
String type;

    public supplier_arraylist(int supplier_id, String supplier_name, String address, String city, String state, String mobile_no, String email, String type) {
        this.supplier_id = supplier_id;
        this.supplier_name = supplier_name;
        this.address = address;
        this.city = city;
        this.state = state;
        this.mobile_no = mobile_no;
        this.email = email;
        this.type = type;
    }

}
