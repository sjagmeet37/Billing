
package customer;


public class customer_arraylist {
    
int customer_id;
String name;
String father_name;
String address;
String city;
String state;
String mobile_no;
String email;
String date;

    public customer_arraylist(int customer_id, String name, String father_name, String address, String city, String state, String mobile_no, String email, String date) {
        this.customer_id = customer_id;
        this.name = name;
        this.father_name = father_name;
        this.address = address;
        this.city = city;
        this.state = state;
        this.mobile_no = mobile_no;
        this.email = email;
        this.date = date;
    }
    
    
}
